package ZhiFeng;

public class Trio{
    private String Sandwich;
    private String Salad;
    private String Drink;

    public trio(String Sandwich, String Salad, String Drink){
        this.Sandwich = Sandwich;
        this.Salad = Salad;
        this.Drink = Drink;
    }
    return Sandwich();
    return Salad();
    return Drink();
}
